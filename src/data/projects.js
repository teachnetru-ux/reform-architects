/**
 * Reform Architects — проекты и категории.
 *
 * Единая структура у ВСЕХ проектов (по образцу Luciano 74):
 *  • tagline  — каталожная строка (короткое описание под названием в каталоге
 *               и в шапке страницы проекта);
 *  • essence  — эссенция (описание под hero на странице проекта);
 *  • context  — Задача / Ограничения / Решение;
 *  • idea + hero — ключевая идея и герой-приём (нижний тёмный блок).
 *
 *  • Мета (площадь · год · статус · город) — выводится, если задана.
 *  • Изображения — слоты (см. resolveImage): кладёте растр с тем же именем в
 *    public/img/ — заглушка заменяется автоматически.
 *  • `featured: true` — проект попадает в избранный грид на главной.
 */

